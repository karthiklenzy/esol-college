				<h3>Our Teachers</h3>

				<p>Having empowered a variety of students over the past decade, we have identified that our success stems from the perfect ‘fit’ between our teachers and the contemporary course delivery mechanisms they adopt within the classroom set-up.</p>

				<p>Our staff members have undergone a one year teacher training programme (TKT) conducted by the British Council, providing them a thorough understanding of teaching methods and child psychology. As a result, our students benefit from high standard classroom sessions that are carried out using the most apt method, for each programme.</p>

				<p>Following a unique ‘activity based’ teaching system, we have over 85 well trained teachers who are not only knowledgeable within their subject matter but they are also passionate tutors who genuinely wish to impart their knowledge to the students. Our teachers undergo training themselves in order to thereafter teach the students on a daily basis. This unique focus on teacher training has enabled our teachers to successfully adhere to the activity based teaching methodology, which has delivered results time and time again.</p>

				<p>Furthermore, our teachers aim to simultaneously develop the four fundamental skills required to excel in the study of the English language. As such, we focus on enhancing the skills of reading, writing, listening and speaking of our students, so that these skills develop concurrently.</p>

				<p>Our core competency is the activity based teaching method which sets us apart from the rest. Therefore with a vision of opening new branches to cater to the rural communities of the Country, our strong focus on teaching will continue to help us carry out the holistic development of many more students, in the years to come.</p>
